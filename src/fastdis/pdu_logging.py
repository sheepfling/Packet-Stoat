HeaderTuple = tuple[int, int, int, int, int, int, int, int]


@dataclass(frozen=True, slots=True)
class FastDisPduLogDescriptor:
    version: int
    pdu_type: int
    canonical_name: str
    aliases: tuple[str, ...]
    protocol_family: int
    family_name: str
    support_level: str
    endpoint_python: str
    endpoint_unreal: str
    endpoint_godot: str
    endpoint_unity: str
    endpoint_lattice_lab: str
    default_log_level: str
    summary_template: str
    diagnostic_tags: tuple[str, ...]
    field_hints: tuple[str, ...]

    def endpoint_behavior(self, endpoint: str) -> str:
        normalized = endpoint.lower()
        if normalized == 'python':
            return self.endpoint_python
        if normalized == 'unreal':
            return self.endpoint_unreal
        if normalized == 'godot':
            return self.endpoint_godot
        if normalized == 'unity':
            return self.endpoint_unity
        if normalized in {'lattice', 'lattice_lab'}:
            return self.endpoint_lattice_lab
        return 'generic_event'


@dataclass(frozen=True, slots=True)
class FastDisLogEvent:
    schema: str
    time_unix_ns: int
    source: str
    stream_id: str
    event_kind: str
    level: str
    code: str
    version: int | None
    exercise_id: int | None
    pdu_type: int | None
    pdu_name: str | None
    protocol_family: int | None
    declared_length: int | None
    packet_size: int
    support_level: str
    endpoint_behavior: str
    diagnostics: tuple[str, ...]
    rate_limited_count: int = 0
    translation_status: str = 'not_requested'
    entity_id: dict[str, int] | None = None


