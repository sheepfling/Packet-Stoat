_DESCRIPTORS_BY_KEY = {(item.version, item.pdu_type): item for item in PDU_LOG_DESCRIPTORS}


def find_pdu_log_descriptor(version: int, pdu_type: int) -> FastDisPduLogDescriptor | None:
    return _DESCRIPTORS_BY_KEY.get((version, pdu_type))


def _now_ns() -> int:
    return time.time_ns()


def _malformed_event(data: bytes | bytearray | memoryview, message: str, source: str, stream_id: str) -> FastDisLogEvent:
    text = message.lower()
    if 'shorter than the 12-byte header' in text:
        code = 'FDIS0300_MALFORMED_TOO_SHORT'
    elif 'exceeds supplied buffer' in text:
        code = 'FDIS0301_MALFORMED_DECLARED_LENGTH_EXCEEDS_DATAGRAM'
    elif 'smaller than the header' in text:
        code = 'FDIS0302_MALFORMED_LENGTH_TOO_SMALL'
    else:
        code = 'FDIS0300_MALFORMED_TOO_SHORT'
    return FastDisLogEvent(
        schema='fastdis.log.pdu.v1',
        time_unix_ns=_now_ns(),
        source=source,
        stream_id=stream_id,
        event_kind='pdu.dropped',
        level='error',
        code=code,
        version=None,
        exercise_id=None,
        pdu_type=None,
        pdu_name=None,
        protocol_family=None,
        declared_length=None,
        packet_size=len(memoryview(data).cast('B')),
        support_level='malformed',
        endpoint_behavior='drop',
        diagnostics=(message,),
    )


def make_pdu_log_event(
    data: bytes | bytearray | memoryview,
    *,
    source: str = 'python',
    endpoint: str = 'python',
    stream_id: str = '',
    event_kind: str = 'pdu.received',
    level: str | None = None,
) -> FastDisLogEvent:
    try:
        header = _fallback.parse_header(data, strict=True)
    except ValueError as exc:
        return _malformed_event(data, str(exc), source, stream_id)
    assert header is not None
    descriptor = find_pdu_log_descriptor(header[0], header[2])
    if descriptor is None:
        return FastDisLogEvent(
            schema='fastdis.log.pdu.v1',
            time_unix_ns=_now_ns(),
            source=source,
            stream_id=stream_id,
            event_kind=event_kind,
            level=level or 'warning',
            code='FDIS0201_PDU_UNKNOWN_TYPE',
            version=header[0],
            exercise_id=header[1],
            pdu_type=header[2],
            pdu_name=None,
            protocol_family=header[3],
            declared_length=header[5],
            packet_size=len(memoryview(data).cast('B')),
            support_level='unknown',
            endpoint_behavior='generic_event',
            diagnostics=('unknown PDU type for protocol version',),
        )
    diagnostics: list[str] = []
    code = 'FDIS0100_PACKET_RECEIVED'
    if descriptor.support_level in {'enum_only', 'schema_patch_required'}:
        code = 'FDIS0202_PDU_SCHEMA_GAP'
        diagnostics.append('known PDU has no generated schema-backed parser')
    elif descriptor.endpoint_behavior(endpoint) in {'generic_pdu_event', 'generic_event', 'pdureceived'}:
        code = 'FDIS0200_PDU_UNSUPPORTED_KNOWN'
        diagnostics.append('known PDU is routed through generic endpoint behavior')
    return FastDisLogEvent(
        schema='fastdis.log.pdu.v1',
        time_unix_ns=_now_ns(),
        source=source,
        stream_id=stream_id,
        event_kind=event_kind,
        level=level or descriptor.default_log_level,
        code=code,
        version=header[0],
        exercise_id=header[1],
        pdu_type=header[2],
        pdu_name=descriptor.canonical_name,
        protocol_family=header[3],
        declared_length=header[5],
        packet_size=len(memoryview(data).cast('B')),
        support_level=descriptor.support_level,
        endpoint_behavior=descriptor.endpoint_behavior(endpoint),
        diagnostics=tuple(diagnostics),
    )


def log_event_to_dict(event: FastDisLogEvent) -> dict[str, Any]:
    payload = asdict(event)
    payload['translation'] = {'status': payload.pop('translation_status')}
    return payload


def log_event_to_json(event: FastDisLogEvent) -> str:
    return json.dumps(log_event_to_dict(event), separators=(',', ':'), sort_keys=True)


def format_log_summary(event: FastDisLogEvent) -> str:
    if event.version is None or event.pdu_type is None:
        reason = event.diagnostics[0] if event.diagnostics else event.code
        return f'[FastDIS] drop malformed packet reason={reason} size={event.packet_size}'
    descriptor = find_pdu_log_descriptor(event.version, event.pdu_type)
    version_label = f'DIS{event.version}'
    if descriptor is None:
        return f'[FastDIS] rx {version_label} unknown pdu={event.pdu_type} ex={event.exercise_id} len={event.declared_length}'
    text = descriptor.summary_template.format(
        version_label=version_label,
        pdu_name=event.pdu_name,
        pdu_type=event.pdu_type,
        exercise_id=event.exercise_id,
        declared_length=event.declared_length,
        endpoint_behavior=event.endpoint_behavior,
        support_level=event.support_level,
    )
    if event.rate_limited_count:
        text = f'{text} rate_limited_count={event.rate_limited_count}'
    return f'[FastDIS] {text}'


class PduLogAggregator:
    def __init__(self) -> None:
        self.total = 0
        self.malformed = 0
        self.unsupported = 0
        self.counts: dict[tuple[int, int], int] = {}

    def record(self, event: FastDisLogEvent) -> None:
        self.total += 1
        if event.version is None or event.pdu_type is None:
            self.malformed += 1
            return
        key = (event.version, event.pdu_type)
        self.counts[key] = self.counts.get(key, 0) + 1
        if event.code in {'FDIS0200_PDU_UNSUPPORTED_KNOWN', 'FDIS0201_PDU_UNKNOWN_TYPE', 'FDIS0202_PDU_SCHEMA_GAP'}:
            self.unsupported += 1

    def summary(self, *, interval_seconds: int = 5) -> str:
        top = sorted(self.counts.items(), key=lambda item: item[1], reverse=True)[:5]
        pdu_counts = ' '.join(f'DIS{version}/pdu{pdu_type}={count}' for (version, pdu_type), count in top)
        if not pdu_counts:
            pdu_counts = 'none'
        return (
            f'[FastDIS] {interval_seconds}s summary rx={self.total} '
            f'unsupported={self.unsupported} malformed={self.malformed} {pdu_counts}'
        )
